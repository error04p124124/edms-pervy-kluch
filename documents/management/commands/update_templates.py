class Command(BaseCommand):
    help = 'Обновить шаблоны документов с реалистичным содержимым и плейсхолдерами'

    def handle(self, *args, **options):
        updated = 0
        skipped = 0

        for template in DocumentTemplate.objects.all():
            data = TEMPLATES_DATA.get(template.name)
            if not data:
                self.stdout.write(self.style.WARNING(f'  Пропущен (нет данных): {template.name}'))
                skipped += 1
                continue

            template.html_template = data['html_template'].strip()
            template.placeholders = data['placeholders']
            template.save(update_fields=['html_template', 'placeholders'])
            self.stdout.write(self.style.SUCCESS(
                f'  ✓ {template.name} ({len(data["placeholders"])} плейсхолдеров)'
            ))
            updated += 1

        self.stdout.write(self.style.SUCCESS(
            f'\nГотово: обновлено {updated}, пропущено {skipped} шаблонов.'
        ))
